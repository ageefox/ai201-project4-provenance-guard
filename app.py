LOG_FILE = "audit_log.json"


def read_log():
    if not os.path.exists(LOG_FILE):
        return []
    with open(LOG_FILE, "r") as f:
        try:
            return json.load(f)
        except json.JSONDecodeError:
            return []


def write_log(entries):
    with open(LOG_FILE, "w") as f:
        json.dump(entries, f, indent=2)


def append_log(entry):
    entries = read_log()
    entries.append(entry)
    write_log(entries)


# ---------------------------------------------------------------------------
# Content store
# ---------------------------------------------------------------------------
CONTENT_FILE = "content_store.json"


def read_store():
    if not os.path.exists(CONTENT_FILE):
        return {}
    with open(CONTENT_FILE, "r") as f:
        try:
            return json.load(f)
        except json.JSONDecodeError:
            return {}


def write_store(store):
    with open(CONTENT_FILE, "w") as f:
        json.dump(store, f, indent=2)


def store_content(content_id, data):
    store = read_store()
    store[content_id] = data
    write_store(store)


def get_content(content_id):
    return read_store().get(content_id)


def update_content_status(content_id, status):
    store = read_store()
    if content_id in store:
        store[content_id]["status"] = status
        write_store(store)
        return True
    return False


